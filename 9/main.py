# Celcius ke Reamur
celcius = float(input('Masukkan suhu dalam celcius: '))
reamur = (4/5) * celcius
print('Suhu dalam reamur adalah: ', reamur)
fahrenhait = (9/5) * celcius + 32
print('Suhu dalam fahrenhait adalah: ', fahrenhait)
kelvin = celcius + 273
print('Suhu dalam kelvin adalah: ', kelvin)